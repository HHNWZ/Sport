package kelvin.tablayout;

public class CommonFoodCaloriesData {
    int common_food_calories_data_image;
    String common_food_calories_data_name;
    String common_food_calories_data_name_unit;
    String common_food_calories_data;

    public CommonFoodCaloriesData(int common_food_calories_data_image, String common_food_calories_data_name, String common_food_calories_data_name_unit, String common_food_calories_data) {
        this.common_food_calories_data_image = common_food_calories_data_image;
        this.common_food_calories_data_name = common_food_calories_data_name;
        this.common_food_calories_data_name_unit = common_food_calories_data_name_unit;
        this.common_food_calories_data = common_food_calories_data;
    }

    public int getCommon_food_calories_data_image() {
        return common_food_calories_data_image;
    }

    public void setCommon_food_calories_data_image(int common_food_calories_data_image) {
        this.common_food_calories_data_image = common_food_calories_data_image;
    }

    public String getCommon_food_calories_data_name() {
        return common_food_calories_data_name;
    }

    public void setCommon_food_calories_data_name(String common_food_calories_data_name) {
        this.common_food_calories_data_name = common_food_calories_data_name;
    }

    public String getCommon_food_calories_data_name_unit() {
        return common_food_calories_data_name_unit;
    }

    public void setCommon_food_calories_data_name_unit(String common_food_calories_data_name_unit) {
        this.common_food_calories_data_name_unit = common_food_calories_data_name_unit;
    }

    public String getCommon_food_calories_data() {
        return common_food_calories_data;
    }

    public void setCommon_food_calories_data(String common_food_calories_data) {
        this.common_food_calories_data = common_food_calories_data;
    }
}
